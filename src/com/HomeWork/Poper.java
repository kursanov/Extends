package com.HomeWork;

public class Poper {
    private String name;
    private int age;
    private String color;
    private String poroda;

    void setName(String PoperName){
        this.name = PoperName;
    }
    void setAge(int PoperAge){
        this.age = PoperAge;
    }
    void setColor(String PoperColor){
        this.color = PoperColor;

    }
    void setPoroda(String PoperPoroda) {
        this.poroda = PoperPoroda;

    }
    String getName() {
        return name;
    }


    int getAge () {
        return age;
    }
    String getColor () {
        return color;

    }
    String getPoroda () {
        return poroda;


    }
}
